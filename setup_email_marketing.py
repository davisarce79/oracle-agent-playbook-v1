if not SKILL_DIR.exists():
    print(f"ERROR: Skill directory not found: {SKILL_DIR}")
    sys.exit(1)

def prompt_or_env(var_name, prompt):
    """Get value from environment if set, else prompt user."""
    val = os.environ.get(var_name)
    if val:
        print(f"{var_name} found in environment.")
        return val
    return input(prompt).strip()

def main():
    print("=== Email Marketing Skill Setup ===\n")
    
    # 1. Gather SMTP credentials
    print("Step 1: Enter SMTP credentials")
    print("(Press Enter to skip if you want to fill manually later)\n")
    
    smtp_host = prompt_or_env("SMTP_HOST", "SMTP host (e.g., smtp.gmail.com: ") or "smtp.gmail.com"
    smtp_port = prompt_or_env("SMTP_PORT", "SMTP port [587]: ") or "587"
    username = prompt_or_env("SMTP_USERNAME", "SMTP username (your email): ")
    password = prompt_or_env("SMTP_PASSWORD", "SMTP password/app password: ")
    from_email = prompt_or_env("FROM_EMAIL", "From email address: ") or username
    from_name = prompt_or_env("FROM_NAME", "From name [Agent Gunna]: ") or "Agent Gunna"
    use_tls = prompt_or_env("SMTP_USE_TLS", "Use TLS? [y/N]: ").lower() in ('y', 'yes', '1')
    
    if not all([username, password]):
        print("\nWARNING: Username and password required for setup. Skipping bake-in.")
        print("You can configure manually using CONFIG.md or re-run this script later.")
        return
    
    # 2. Create send_campaign.py (baked config)
    campaign_script = BASE_DIR / "send_campaign.py"
