# Load recipients from CSV (default: recipients.csv)
def load_recipients(csv_path):
    import csv
    recipients = []
    with open(csv_path, newline='', encoding='utf-8') as f:
        reader = csv.DictReader(f)
        for row in reader:
            recipients.append({{"email": row["email"].strip(), "name": row.get("name", "").strip()}})
    return recipients

def main():
    if len(sys.argv) < 2:
        print("Usage: python send_campaign.py <recipients.csv> <subject> <body_template_file>")
        print("Example: python send_campaign.py recipients.csv 'Check this out' body.txt")
        sys.exit(1)
    
    csv_path = sys.argv[1]
    subject = sys.argv[2]
    template_file = sys.argv[3] if len(sys.argv) > 3 else None
    
    if template_file:
        with open(template_file, encoding='utf-8') as f:
            body_template = f.read()
    else:
        body_template = input("Enter body template (use {{name}} for personalization):\\n")
    
    recipients = load_recipients(csv_path)
    print(f"Loaded {{len(recipients)}} recipients from {{csv_path}}")
    
    result = send_batch_email(
        **SMTP_CONFIG,
        subject=subject,
        body_template=body_template,
        recipients=recipients
    )
    
    print(json.dumps(result, indent=2))

